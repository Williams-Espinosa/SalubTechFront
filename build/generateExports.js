#!/usr/bin/env node
// build/generateExports.js
// Ejecuta: npm run generate-exports
// Genera src/constants/exportFiles.js con los archivos XLS y PDF en base64.
//
// Requiere Python + openpyxl + reportlab:
//   pip install openpyxl reportlab

import { execSync } from "child_process";
import { readFileSync, writeFileSync, mkdirSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT      = join(__dirname, "..");
const TMP       = join(__dirname, "tmp");

mkdirSync(TMP, { recursive: true });

console.log("⚙️  Generando archivos de exportación...");

// ── Llama al script Python que genera xlsx y pdf ──────────────────────────────
const pyScript = join(__dirname, "generateFiles.py");
execSync(`python3 ${pyScript} --out ${TMP}`, { stdio: "inherit" });

// ── Lee los archivos generados y los convierte a base64 ───────────────────────
const xlsx = readFileSync(join(TMP, "bitacora.xlsx")).toString("base64");
const pdf  = readFileSync(join(TMP, "reporte_auditoria.pdf")).toString("base64");

// ── Escribe exportFiles.js ────────────────────────────────────────────────────
const output = `// AUTO-GENERATED by build/generateExports.js — no editar manualmente
export const XLSX_B64 = "${xlsx}";
export const PDF_B64  = "${pdf}";
`;

writeFileSync(join(ROOT, "src/constants/exportFiles.js"), output, "utf8");
console.log("✅  src/constants/exportFiles.js generado correctamente.");
